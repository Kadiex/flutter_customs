    selectedCountryName = countries.first.name;
    selectedCountryCode = countries.first.code;
    selectedCountryFlagPath = countries.first.flagImagePath;
    selectedCountryFormat = countries.first.phoneformat;
  }

  void checkForMissingFlags(List<String> websiteCountries) {
    List<String> existingCountries =
        countries.map((country) => country.name).toList();

    for (var country in websiteCountries) {
      if (!existingCountries.contains(country)) {
        if (kDebugMode) {
          print('Missing flag for: $country');
        }
      }
    }
  }

  void checkForDuplicateFlags() {
    final List<String> flagPaths =
        countries.map((country) => country.flagImagePath).toList();
    final Set<String> uniqueFlags = <String>{};

    for (var path in flagPaths) {
      if (!uniqueFlags.add(path)) {
        if (kDebugMode) {
          print('Duplicate flag found: $path');
        }
      }
    }
  }
